require 'spec_helper'


describe Gui do
  
  before(:each) do
    @gui = Gui.new
    @b = Board.new
    @board = @b.create_board
  end 
  
  it "should print out an simple text display of the board" do
        @gui.render_board(@board).should == "\n         0     1     2     3     4     5     6    7    \n\n" +    
                                                 "      -------------------------------------------------\n" +
                                                 "  0   |  R  |  #  |  R  |  #  |  R  |  #  |  R  |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  1   |  #  |  R  |  #  |  R  |  #  |  R  |  #  |  R  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  2   |  R  |  #  |  R  |  #  |  R  |  #  |  R  |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  3   |  #  |     |  #  |     |  #  |     |  #  |     |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  4   |     |  #  |     |  #  |     |  #  |     |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  5   |  #  |  B  |  #  |  B  |  #  |  B  |  #  |  B  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  6   |  B  |  #  |  B  |  #  |  B  |  #  |  B  |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  7   |  #  |  B  |  #  |  B  |  #  |  B  |  #  |  B  |  \n" +
                                                 "      -------------------------------------------------\n\n"
    
  end

  it "should print a BK or RK for black king checkers or red king checkers" do
       @board[0][0].make_king
       @board[0][0].color = :black
       @board[7][1].make_king
       @board[7][1].color = :red
       @gui.render_board(@board).should == "\n         0     1     2     3     4     5     6    7    \n\n" +    
                                                 "      -------------------------------------------------\n" +
                                                 "  0   |  BK |  #  |  R  |  #  |  R  |  #  |  R  |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  1   |  #  |  R  |  #  |  R  |  #  |  R  |  #  |  R  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  2   |  R  |  #  |  R  |  #  |  R  |  #  |  R  |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  3   |  #  |     |  #  |     |  #  |     |  #  |     |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  4   |     |  #  |     |  #  |     |  #  |     |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  5   |  #  |  B  |  #  |  B  |  #  |  B  |  #  |  B  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  6   |  B  |  #  |  B  |  #  |  B  |  #  |  B  |  #  |  \n" +
                                                 "      -------------------------------------------------\n" +
                                                 "  7   |  #  |  RK |  #  |  B  |  #  |  B  |  #  |  B  |  \n" +
                                                 "      -------------------------------------------------\n\n"
    
  end
end
